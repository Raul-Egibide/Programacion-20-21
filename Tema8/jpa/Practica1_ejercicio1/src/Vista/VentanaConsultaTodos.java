     private ArrayList<ArrayList<String>> datos;
     private int posicion=0;
     
    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        try
        {
            datos = Controlador.hacerConsultaTodasPersonas();
            actualizarPersona();
        }
        catch(Exception e)
        {
            
        }
    }//GEN-LAST:event_formWindowOpened

    private void bAtrasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bAtrasActionPerformed
        if(posicion>0)
        {
            posicion--;
            actualizarPersona();
        }
    }//GEN-LAST:event_bAtrasActionPerformed

    private void bAdelanteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bAdelanteActionPerformed
        if(posicion!=(datos.size()-1))
        {
            posicion++;
        }
        actualizarPersona();
    }//GEN-LAST:event_bAdelanteActionPerformed

    private void actualizarPersona()
    {
        labelVariable.setText("Mostrando persona "+(posicion+1)+" de "+datos.size());
        tfDni.setText(datos.get(posicion).get(0));
        tfNombre.setText(datos.get(posicion).get(1));
        tfEdad.setText(datos.get(posicion).get(2));
        tfProfesion.setText(datos.get(posicion).get(3));
        tfTelefono.setText(datos.get(posicion).get(4));
    }
    
