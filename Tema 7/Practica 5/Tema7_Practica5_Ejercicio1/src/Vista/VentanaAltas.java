    private boolean comprobarFormatoFechaAlta()
    {
        Pattern p = Pattern.compile("^[0-9]{2}/[0-9]{2}/[0-9]{4}$");
        Matcher m = p.matcher(tfFechaAlta.getText());
        if(m.matches())
        {
            tfFechaAlta.setBorder(javax.swing.BorderFactory.createLineBorder(new java.awt.Color(0, 0, 0)));
            return true;
        }
        else
        {
            tfFechaAlta.setBorder(javax.swing.BorderFactory.createLineBorder(new java.awt.Color(255, 0, 0)));
            return false;
        }
    }
    
    
