    private void bAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bAceptarActionPerformed
        boolean dniCorrecto=comprobarFormatoDni();
        boolean nssCorrecto=comprobarFormatoNss();
        boolean nombreApellidoCorrecto=comprobarFormatoNombreApellidos();
        boolean direccionCorrecto=comprobarFormatoDireccion();
        boolean telefonoCorrecto=comprobarFormatoTelefono();
        boolean sexoCorrecto=comprobarSexoSelected();
        boolean estadoCivilCorrecto=comprobarEstadoCivilSelected();
        boolean contratoCorrecto=comprobarContratoSelected();
        boolean departamentoCorrecto=comprobarDepartamentoSelected();
        boolean fechaCorrecto=comprobarFormatoFechaAlta();
        if(dniCorrecto && nssCorrecto && nombreApellidoCorrecto && direccionCorrecto 
                && telefonoCorrecto && sexoCorrecto && estadoCivilCorrecto && contratoCorrecto && departamentoCorrecto && fechaCorrecto)
        {
            String nombre="";
            String apellidos="";
            boolean primerEspacio=false;
            int espacioint=0;
            for(int x=0; x<tfNombreApellidos.getText().length();x++)
            {
                if(!primerEspacio && tfNombreApellidos.getText().charAt(x)==' ')
                {
                    espacioint=x;
                    primerEspacio=true;
                }
                if(!primerEspacio)
                {
                    nombre+=tfNombreApellidos.getText().charAt(x);
                }
                if(primerEspacio && espacioint!=x)
                {
                    apellidos+=tfNombreApellidos.getText().charAt(x);
                }
            }
            char sexo;
            if(rbHombre.isSelected())
            {
                sexo='H';
            }
            else
            {
                sexo='M';
            }
            String estadoCivil;
            if(rbSoltero.isSelected())
            {
                estadoCivil="Soltero";
            }
            else
            {
                estadoCivil="Casado";
            }
            Controlador.realizarAlta(tfDni.getText(),tfNss.getText(),nombre,apellidos,sexo,estadoCivil,tfDireccion.getText(),tfTelefono.getText(),cbTipoContrato.getSelectedIndex(),cbDepartamento.getSelectedIndex(),tfFechaAlta.getText());
        }
    }//GEN-LAST:event_bAceptarActionPerformed

