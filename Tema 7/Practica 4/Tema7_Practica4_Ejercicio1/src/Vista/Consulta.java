    private void actualizarLabels(ArrayList<String> textos, String saldo)
    {
        lSaldo.setText("SALDO:" + saldo);
        lSaldo.setVisible(true);
        ArrayList<JLabel> labels = new ArrayList();
        labels.add(lMov1);
        labels.add(lMov2);
        labels.add(lMov3);
        labels.add(lMov4);
        labels.add(lMov5);
        for(int x=0; x<textos.size(); x++)
        {
            labels.get(x).setText(textos.get(x));
            labels.get(x).setVisible(true);
        }
        
    }
    
