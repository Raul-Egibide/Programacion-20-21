    private void tfProductoFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_tfProductoFocusLost
        if(Controlador.comprobarDatosNoValidosNombre(tfProducto.getText()))
        {
            tfProducto.setBorder(javax.swing.BorderFactory.createLineBorder(new java.awt.Color(255, 0, 0)));
            tfProducto.setText("");
        }
        else
        {
            tfProducto.setBorder(javax.swing.BorderFactory.createLineBorder(new java.awt.Color(0, 0, 0)));
            tfUnidades.setEditable(true);
        }
    }//GEN-LAST:event_tfProductoFocusLost

    private void tfUnidadesFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_tfUnidadesFocusLost
        if(tfUnidades.isEnabled() && tfUnidades.isEditable())
        {
            if(Controlador.comprobarDatosNoValidosNumero(tfUnidades.getText(),tfProducto.getText()))
            {
                tfUnidades.setBorder(javax.swing.BorderFactory.createLineBorder(new java.awt.Color(255, 0, 0)));
                tfUnidades.setText("");

            }
            else
            {
                tfUnidades.setBorder(javax.swing.BorderFactory.createLineBorder(new java.awt.Color(0, 0, 0)));
                tfImporte.setText(Controlador.setImporte(tfProducto.getText(),tfUnidades.getText()));
                bAceptar.setEnabled(true);
            }
        }
        
    }//GEN-LAST:event_tfUnidadesFocusLost

